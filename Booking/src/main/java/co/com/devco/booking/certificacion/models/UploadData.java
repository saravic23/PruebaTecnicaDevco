package co.com.devco.booking.certificacion.models;

public class UploadData {
    private String name;
    private String phone;
    private String gender;
    private String date;
    private String email;
    private String password;

    public UploadData(String name, String phone, String gender, String date, String email, String password) {
        this.name = name;
        this.phone = phone;
        this.gender = gender;
        this.date = date;
        this.email = email;
        this.password = password;
    }





}
